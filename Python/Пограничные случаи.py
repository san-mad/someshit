def is_arguments_for_substr_correct(string, index, length):
    if not isinstance(string, str):
        return False
    if not isinstance(index, int) or not isinstance(length, int):
        return False
    if length < 0:
        return False
    if index < 0:
        return False
    if index >= len(string):
        return False
    if index + length > len(string):
        return False
    return True


